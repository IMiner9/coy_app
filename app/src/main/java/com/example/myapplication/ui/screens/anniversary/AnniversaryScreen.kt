) {
    val daysUntil = remember(anniversary.date) {
        try {
            val anniversaryDate = LocalDate.parse(anniversary.date, DateTimeFormatter.ISO_LOCAL_DATE)
            val today = LocalDate.now()
            ChronoUnit.DAYS.between(today, anniversaryDate).toInt()
        } catch (e: Exception) {
            null
        }
    }
    
    var showDeleteConfirm by remember { mutableStateOf(false) }

    val baseColor = remember(anniversary.color) {
        parseColorOrFallback(anniversary.color, Color(0xFFFFE0E0))
    }
    val containerColor = baseColor.copy(alpha = 0.25f)
    val accentColor = baseColor.copy(alpha = 0.55f)
