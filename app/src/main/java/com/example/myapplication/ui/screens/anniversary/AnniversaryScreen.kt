                }
            }
        )
    }
}

// ÏûêÎèô Í∏∞ÎÖêÏùº Í≥ÑÏÇ∞ Ìï®Ïàò
fun calculateAutoAnniversaries(startDate: LocalDate): List<Anniversary> {
    val today = LocalDate.now()
    val anniversaries = mutableListOf<Anniversary>()
    
    // 100Ïùº, 200Ïùº, 300Ïùº, ... Îã®ÏúÑ Í∏∞ÎÖêÏùº
    for (days in 100..1000 step 100) {
        val anniversaryDate = startDate.plusDays(days.toLong())
        if (!anniversaryDate.isBefore(today)) { // Ïò§Îäò Ïù¥ÌõÑ ÎÇ†ÏßúÎßå Ï∂îÍ∞Ä
            anniversaries.add(
                Anniversary(
                    title = "${days}Ïùº",
                    date = anniversaryDate.format(DateTimeFormatter.ISO_LOCAL_DATE),
                    color = DEFAULT_ANNIVERSARY_COLOR_HEX,
                    isAutoGenerated = true
                )
            )
        }
    }
    
    // Ï£ºÎÖÑ Í∏∞ÎÖêÏùº (1Ï£ºÎÖÑ, 2Ï£ºÎÖÑ, ...)
    for (year in 1..10) {
        val anniversaryDate = startDate.plusYears(year.toLong())
        if (!anniversaryDate.isBefore(today)) { // Ïò§Îäò Ïù¥ÌõÑ ÎÇ†ÏßúÎßå Ï∂îÍ∞Ä
            anniversaries.add(
                Anniversary(
                    title = "${year}Ï£ºÎÖÑ",
                    date = anniversaryDate.format(DateTimeFormatter.ISO_LOCAL_DATE),
                    color = DEFAULT_ANNIVERSARY_COLOR_HEX,
                    isAutoGenerated = true
                )
            )
        }
    }
    
    return anniversaries.sortedBy { it.date }
}

